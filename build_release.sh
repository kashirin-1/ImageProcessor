#!/bin/bash

# Обычная сборка без санитайзеров

set -e  # Прерывать выполнение при ошибке

echo "=== Обычная сборка проекта ==="

# Создаем директорию для сборки если её нет
mkdir -p build
cd build

# Настраиваем CMake
echo "Настройка CMake..."
cmake ..

# Собираем проект
echo "Сборка проекта..."
make -j$(nproc)

# Копируем бинарник в корень проекта
echo "Копирование бинарника в корень проекта..."
cp image_processor ../
cd ..

echo "=== Сборка завершена ==="
echo "Исполняемый файл: ./image_processor"